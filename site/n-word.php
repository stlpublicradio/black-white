      <div class="col-md-8">

            <p>One of the most poisonous words in the English language is the word “nigger,” the “N” word. It is a racist, demeaning, humiliating slur whose use has moved from everyday to rare. A new edition of Mark Twain’s <em>Huckleberry Finn</em> is removing it entirely. However, it is still very much a part of the American vocabulary. In modern America, it is often used by black entertainers in comedy routines and popular rap music. And, blacks have been known to use it derogatorily against each other. Whites avoid it in polite conversation and shun it in public discourse. Nonetheless, it is used in private conversation among some people. Racist comments by popular broadcast personality Don Imus and movie star Mel Gibson ignited a national conversation on the “N” word and the language of race. It prompted a discussion with leaders of the black community in St. Louis: Reverend Douglas Parham, President of the St. Louis Metropolitan Clergy Coalition; John Moton, Jr., who chairs the St. Louis Black Leadership Roundtable; and James Buford, President and CEO of the Urban League of Metropolitan St. Louis.</p>
            <p>Contributors: <strong>Reverend Douglas Parham</strong>, <strong>John Moton, Jr</strong>, and <strong>James Buford</strong></p>
