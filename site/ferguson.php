          <h1 class="top"><strong>Ferguson <span class="date">August 9, 2014 - 2016</span></h1>
          <p>The August 9, 2014 shooting death of Michael Brown in Ferguson by police officer Darren Wilson led to an immediate and prolonged response. People began street demonstrations and protests against the shooting, and in support of complaints of racial inequity and injustice. Local demonstrations led to a national movement called “Black Lives Matter,”  and to extensive discussion of civil rights issues in modern America. St. Louis on the Air is proud to have been a part of these discussions. Continuing St. Louis Public Radio’s “St. Louis History in Black and White” project, what follows are some of the programs and segments devoted to the Ferguson story.</p>
