        </div>
      <div class="col-md-8">

            <p class="top">One of the most important chapters in St. Louis civil rights history was written in the late summer of 1963 when African-Americans launched a series of protest demonstrations against the Jefferson Bank. It was largely a community effort by black political, religious and activist leaders in protest against not only the lack of black employees at the bank, but for the widespread practice against employing Black Americans by many of the major companies in the city. One of the key players in the demonstrations was a city Alderman, William Clay, who later was elected to Congress. He and hundreds of others went to jail for their role in the demonstrations, the legal ramifications of which went on for three and a half years. Clay documented the confrontation in his book <em><a href="http://www.amazon.com/The-Jefferson-Bank-Confrontation-Struggle/dp/B001ONZUG6">The Jefferson Bank Confrontation: The Struggle for Civil Rights in St. Louis</a></em>. He talked about it with his son Congressman William (Lacy) Clay.</p>
