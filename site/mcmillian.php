            <p class="top">Not all those who influenced civil rights and racial understanding in the St. Louis area are necessarily well known outside their own professional or personal community. Judge Theodore McMillian, who died in 2006 after 50 years as a highly respected attorney and judge, is an example. He was always a champion of the disadvantaged. Judge McMillian was the grandson of slaves who grew up poor in St. Louis. He was a man of many firsts: as one of the first black students at the St. Louis University School of Law, he graduated first in his class; he also was the first African-American assistant prosecutor for the City of St. Louis, and later the cityâ€™s first black judge. He was the first black member of the Missouri Court of Appeals-Eastern District and the first African-American to reach the U.S. Circuit Court of Appeals. One of his early clerks was St. Louis Circuit Court Judge Lisa Van Amberg, who remembers that Judge McMillian wrote landmark decisions on desegregation, free speech, civil rights, employment discrimination, and affirmative action.</p>
