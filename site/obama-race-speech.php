            <p class="top">During the 2008 presidential campaign, then Senator Barack Obama delivered what was largely considered his most important speech of the campaign, and one of the most significant speeches ever on the subject of race relations. He was responding to comments made by his long-time pastor, Rev. Jeremiah Wright condemning what he called “racist America.” Wright suggested that racism led to the September 11 attack at the World Trade Center. Because he was an informal adviser to the Obama campaign, Wright’s comments raised questions about candidate Obama’s own attitude on race. Sen. Obama delivered his response, titled <em>A More Perfect Union</em> at Philadelphia’s Constitution Center. The candidate discussed racial tension, white privilege, and black anger. He appealed for America and Americans to “come together” to confront and solve the major problems facing <em>all</em> Americans. The speech launched a national dialogue on race. In St. Louis, the polarization of races had long been acknowledged by both black and white people. Part of the local dialogue was conducted by Lewis Reed, President of the Board of Aldermen, Rabbi Mark Shook of Temple Israel, and Reverend Earl Nance, Jr. of the Greater Mt. Carmel Baptist Church.</p>
