        </div>
      <div class="col-md-8">

            <p class="top">The flurry of Constitutional Amendments after the Civil War was designed to offer the Constitutional protection that had been denied African-Americans since before the Republic was founded. Of particular importance were the 14th Amendment’s equal protection and due process clauses. It was designed to overturn the Dred Scott ruling by the U.S. Supreme Court, which held in 1857 that held that blacks could not be citizens of the United States. But the 14th Amendment did not end discrimination in some areas for decades. In spite of Constitutional protections, a practice called “restrictive covenants” prevented Black Americans from living in certain areas. Restrictive covenants were widespread in St. Louis. It took a landmark St. Louis case almost a century after the 14th Amendment to fracture the practice of these covenants. It was known as the Shelley vs. Kraemer case and was detailed by author/historian Jeffrey Copeland, the author of <em><a href="http://www.amazon.com/Olivias-Story-Conspiracy-Shelley-Kraemer/dp/1557788847">Olivia’s Story</a></em>.</p>
