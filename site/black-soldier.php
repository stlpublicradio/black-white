      <div class="col-md-8">
    <p class="top">Much has been written about black military units in this country, from the Buffalo Soldiers after the Civil War to the Tuskegee Airmen of World War II. Less has been written about the experience of individual black soldiers who played a significant role in Europe and the Pacific during World War II before the Armed Services were integrated in 1948. University of Iowa Professor Jeffrey Copeland has written about one such soldier, Inman Perkins, who was a teacher at St. Louis’ Sumner High when he was drafted at the start of the war. Copeland recounts Perkins’ experiences, which mirrored those of many black servicemen, in his book, <i>Inman’s War: A Soldier’s Story of Life in a Colored Battalion in World War II</i>.</p>
