            <p class="top">St. Louis has the dubious distinction of being one of the most racially polarized cities in the nation. However, it would be unfair not to acknowledge that many other cities also have had, and still have, problems with race relations. Three black African-Americans of separate generations sat down together at the St. Louis History Museum to talk about their experiences growing up black in St. Louis, and their impressions of how the black experience changed for them here over the years. Mariah Richardson is a playwright, actress, and teacher. She attended desegregated schools in St. Louis. Donn Johnson is a retired broadcast journalist who grew up on both sides of the Brown vs. Board of Education decision. Percy Green is a lifelong civil rights activist who worked primarily as a civil disobedience strategist. He was in the vanguard of the civil rights movement in the late fifties through the early sixties, and ever since.</p>
            <p>Contributors: <strong>Mariah Richardson</strong>, <strong>Donn Johnson</strong>, and <strong>Percy Green</strong></p>
