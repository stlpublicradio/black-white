          <h3>Introduction</h3>
          <h4>by St. Louis on the Air Host Don Marsh</h4>
          <p>In August of 2014, the shooting death of Michael Brown Jr. in Ferguson, Missouri, ripped a scab from the long tenuous and often tortured relationship between black and white people, revealing that race relations were still in need of significant mending. Not only here in St. Louis, but all over the country.</p>
          </div>
          </div>
          </div>

          <div class="item">
          <div class="carousel-content">
          <div>
          <p>Missouri in general, and the St. Louis region in particular, became a symbol of racial inequity, as well as resulting economic and social justice disparities.</p>
          </div>
          </div>
          </div>

          <div class="item">
          <div class="carousel-content">
          <div>
          <p>Ferguson brought new attention to the existence of these disparities all across the nation. It gave a bigger voice to the Black Lives Matter movement, whose protestors demonstrated across the country, demanding a level playing field for all. The movement was given significant impetus by the shooting deaths of numerous young Black men at the hands of White police officers in the months following Brown’s death. The country experienced an increased degree of racial polarization: polarization that had existed either undetected or ignored just below, and sometimes not so far below, the surface of American society.</p>
          </div>
          </div>
          </div>

          <div class="item">
          <div class="carousel-content">
          <div>
          <p>That Missouri has played a key role in this ongoing drama should come as no surprise. It has been a big part of the drama for a long, long time. </p>
          </div>
          </div>
          </div>

          <div class="item">
          <div class="carousel-content">
          <div>
          <p>Missouri and St. Louis have a history of indecisiveness on the issue of race relations. Two centuries ago, although Missouri was a slave state, there was no unanimity on the question of abolishing slavery or maintaining it. Tension was often evidenced by violence and vitriol; there were distinct points of view and champions of both. Slavery in the state had been legitimized by practice, precedent, and politics. But there were also challengers: those of both races who refused to accept the status quo.</p>
          </div>
          </div>
          </div>

          <div class="item">
          <div class="carousel-content">
          <div>
          <p>During the mid-19th century, slavery was being debated all over the United States. Abolitionists were active, and actively countered by the proponents of slavery. In the years leading to the Civil War, the Dred Scott case put St. Louis and Missouri in the middle of the discussion. While this Supreme Court case may have been a significant contributor to the war between the states, it may also have given evidence to a less publicized version of history: that white people here were active in the cause of Dred and Harriet Scott and their two children.</p>
          </div>
          </div>
          </div>

          <div class="item">
          <div class="carousel-content">
          <div>
          <p>The Civil War led to an attempt to shift the paradigm of race relations. The 13th, 14th, and 15th Amendments to the Constitution were designed to do just that.</p>
          </div>
          </div>
          </div>

          <div class="item">
          <div class="carousel-content">
          <div>
          <p>They did not. Reconstruction followed, then Jim Crow, then the modern era. Tension between the races continued through the 19th and 20th centuries. Indeed, it remains today in the 21st century, despite the 2008 election of America’s first African-American president. Some hoped the election of Barack Obama would bring with it a “post-racial” America. Clearly, it did not. </p>
          </div>
          </div>
          </div>

          <div class="item">
          <div class="carousel-content">
          <div>
          <p>This collection is an attempt to bring that history from the Dred and Harriet Scott case, through and beyond the Michael Brown shooting. </p>
          </div>
          </div>
          </div>

          <div class="item">
          <div class="carousel-content">
          <div>
          <p>For white people in St. Louis there is much to be both ashamed of and proud of in the region’s history of race relations. For black people, there is much of which to be proud … and embittered.</p>
          </div>
          </div>
          </div>

          <div class="item">
          <div class="carousel-content">
          <div>
          <p>What follows is not intended to be a complete and detailed history of the period from 1847 to 2016. Rather, St. Louis Public Radio covers those years through conversations with historians, authors, and first-person interviews with people who lived some of that history. We have included interviews from 2005 to 2016, including detailed conversations on Ferguson and its aftermath.</p>
          </div>
          </div>
          </div>

          <div class="item">
          <div class="carousel-content">
          <div>
          <p>For the most part, the focus is not on well-known “celebrities” of the civil rights movement, but rather on average people, black and white, who worked hard to eradicate the formal and informal barriers to integration. The intention is to enlighten black and white people on histories with which they may not be familiar, and to explore the environment in which we are living today, post-Ferguson. It is our hope it will inspire greater understanding and harmony between the races in our community today. Hopefully, these stories will also encourage a more detailed look at the events included.</p>
          </div>
          </div>
          </div>


