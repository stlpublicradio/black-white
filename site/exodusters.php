      <div class="col-md-8">

            <p class="top">During the years immediately following Reconstruction, 1879 and 1880, formerly enslaved people migrated from the South to Kansas. Kansas was a common destination because it was a free state, it welcomed settlement by people of all races, and it held symbolic importance as the home state of abolitionist John Brown. This migration was an “exodus.” The migrants who participated were called “Exodusters.” More than 40,000 southern black people&mdash;mostly poor and uneducated&mdash;migrated to Kansas. Half of them came through St. Louis where they were aided by the local African-American community and largely reviled by the white St. Louis civic and political establishment. It was all chronicled in the book <i><a href="http://www.amazon.com/Louis-African-American-Community-Exodusters/dp/0826217729">The African American Community and the Exodusters</a></i>, written by Professor Bryan Jack of Winston-Salem State University in North Carolina.</p>
